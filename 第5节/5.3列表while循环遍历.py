def list_while_func():
    list1 = ['你好', '你好吗', '你真好']
    index = 0
    while index < len(list1):
        element = list1[index]
        print(f"列表的元素：{element}")
        index += 1

list_while_func()

def list_for_func():
    list2 = [1, 8, 3, 7, 5]
    for index in list2:
        print(f"列表的元素有：{index}")

list_for_func()

