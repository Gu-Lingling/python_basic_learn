# 各类运算符
print("1 + 1 = ", 1 + 1)
print("9 - 1 = ", 9 - 1)
print("7 * 5 = ", 7 * 5)
print("7 / 2 = ", 7 / 2)
print("18 // 4 = ", 18 // 4)
print("80 % 9 = ", 80 % 9)
print("7**3 = ", 7**3)

# 赋值运算符
num = 1 + 2 + 3 + 4*1 + 5**1
print("num = ", num)

num += 6   # num = num + 6
print("num += ", num)

num -= 5   # num = num - 5
print("num -= ", num)

num *= 2
print("num *= ", num)

num /= 5
print("num /= ", num)

num **= 5
print("num **= ", num)

num //= 7
print("num //= ", num)