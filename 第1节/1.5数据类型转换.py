"""
数据类型转换函数
int()
float()
str()
"""


# 整数转字符串
num_str = str(11)
print(type(num_str), num_str)

# 浮点数转字符串
float_str = str(3.14)
print(type(float_str), float_str)

# 字符串转换成数字
num = int("11")
print(type(num), num)

# 整数转浮点数
aa = 1111
num2 = float(aa)
print(type(num2), num2)

# 浮点数转整数
num3 = int(3.1415926)
print(type(num3), num3)
